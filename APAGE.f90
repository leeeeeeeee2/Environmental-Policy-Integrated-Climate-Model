      SUBROUTINE APAGE(IP)
!     EPIC1102
!     THIS SUBPROGRAM CHANGES PAGES, WRITES VERSION, DATE, & TITLE
      USE PARM
      WRITE(KW(1),1)IYER,IMON,IDAY,IT1,IT2,IT3
      WRITE(KW(1),2)TITLE
      WRITE(KW(1),3)IRUN,IRO0,IGN
      IF(IP==0)RETURN
      WRITE(KW(1),4)SITEFILE
      WRITE(KW(1),4)SOILFILE
      WRITE(KW(1),4)OPSCFILE
      RETURN
    1 FORMAT('1'/T5,'EPIC1102 v2020-01-23',2X,3I4,2X,2(I2,':'),I2)
    2 FORMAT(/(10X,20A4))
    3 FORMAT(5X,'RUN #=',I4,2X,'ROT #=',I4,2X,'GNSD #=',I4)
    4 FORMAT(5X,A80)
      END